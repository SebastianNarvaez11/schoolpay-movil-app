export const initialCharge = (e) => {
    if (e === 10) {
        return 'Feb-Nov'
    } else if (e === 9) {
        return 'Mar-Nov'
    } else if (e === 8) {
        return 'Abr-Nov'
    } else if (e === 7) {
        return 'May-Nov'
    } else if (e === 6) {
        return 'Jun-Nov'
    } else if (e === 5) {
        return 'Jul-Nov'
    } else if (e === 4) {
        return 'Agos-Nov'
    } else if (e === 3) {
        return 'Sep-Nov'
    } else if (e === 2) {
        return 'Oct-Nov'
    }
}